window.SurveyUtil = (() => {
  const allMetricItems = () => [
    ...window.SURVEY.s2Groups.flatMap((group) => group.items.map(([key, label]) => ({ section: 's2', key, label: `${group.title}: ${label}` }))),
    ...window.SURVEY.s3Items.map(([key, label]) => ({ section: 's3', key, label })),
    ...window.SURVEY.s4Items.map(([key, label]) => ({ section: 's4', key, label }))
  ];

  const valuesFor = (responses, section, key) => responses
    .map((row) => Number(row.payload?.[section]?.[key]))
    .filter((value) => Number.isFinite(value) && value >= 1 && value <= 5);

  const stats = (values) => {
    const n = values.length;
    if (!n) return { n: 0, mean: null, sd: null, counts: { 5: 0, 4: 0, 3: 0, 2: 0, 1: 0 } };
    const mean = values.reduce((sum, value) => sum + value, 0) / n;
    const variance = n > 1 ? values.reduce((sum, value) => sum + Math.pow(value - mean, 2), 0) / (n - 1) : 0;
    return {
      n,
      mean,
      sd: Math.sqrt(variance),
      counts: [5, 4, 3, 2, 1].reduce((acc, value) => ({ ...acc, [value]: values.filter((v) => v === value).length }), {})
    };
  };

  const summarize = (responses) => allMetricItems().map((item) => ({
    ...item,
    ...stats(valuesFor(responses, item.section, item.key))
  }));

  const fmt = (value) => value === null || value === undefined || Number.isNaN(value) ? '-' : value.toFixed(2);
  const flag = (mean) => mean === null ? '' : mean >= 4.5 ? 'จุดแข็ง' : mean < 3.5 ? 'ควรปรับปรุง' : '';
  const html = (text) => String(text ?? '').replace(/[&<>"']/g, (char) => ({ '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;', "'": '&#39;' }[char]));
  const today = () => new Date().toISOString().slice(0, 10).replaceAll('-', '');

  return { allMetricItems, valuesFor, stats, summarize, fmt, flag, html, today };
})();

